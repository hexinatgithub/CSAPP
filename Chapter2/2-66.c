#include <stdio.h>

int leftmost_one(unsigned x){
	x |= x >> 1;
	x |= x >> 2;
	x |= x >> 4;
	x |= x >> 8;
	x |= x >> 16;
	/* 
	以上代码会产生[0...01...1]的格式 
	过程如下:
		假设x格式是[0...01**********]，1是最左边的1
		第一步: x>>1复制最左边的那个1,[0...011*******]
		第二步: x>>2会复制最左的那两个2，产生[0...01111********]
		依次类推
		最后一步: x>>16则会复制最左边的那16个1，产生[0...01...1]
		因为假设w=32所以在最左边1的后面全是1,产生[0...01...1]
	*/
	//在把最左边1的后面的1减掉
	return x - (x >> 1);
}